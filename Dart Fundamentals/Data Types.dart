void main() {
  // int  whole numbers
  int age = 25;
  print("Age: $age");

  // double  decimal numbers
  double height = 5.9;
  print("Height: $height");

  // num  can be int or double
  num score1 = 90;      // int
  num score2 = 95.5;    // double
  print("Scores: $score1 and $score2");

  // bool  true or false
  bool isStudent = true;
  print("Is student? $isStudent");

  // String  text
  String name = "Samraj";
  print("Name: $name");
}
