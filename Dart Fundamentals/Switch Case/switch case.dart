void main() {
  String grade = "B";

  switch (grade) {
    case "A":
      print("Excellent!");
      break;
    case "B":
      print("Good job!");
      break;
    case "C":
      print("You passed.");
      break;
    case "D":
      print("Needs improvement.");
      break;
    default:
      print("Invalid grade.");
  }
}


