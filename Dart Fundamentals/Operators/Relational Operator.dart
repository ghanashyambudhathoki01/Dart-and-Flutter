// There are lots of assignment operators such as : 
// Operator     Meaning           Example   Result      
//  ==           Equal to             5==5      True            
//  !=           Not Equal             5!=3      True           
// >            Greater than           5>3       True              
// <            Less than              5<3       False             
// >=           Greater than or equal   5>=5     True
// <=           Less than or equal      5<=3     False     
            
// Simple Greater than example : 
import "dart:io";
void main (){
  var a = 10;
  var b = 12;
  print (a>b);
}